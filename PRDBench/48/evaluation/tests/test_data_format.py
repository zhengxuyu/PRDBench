def test_coordinate_consistency():
    """Test coordinate system consistency"""
    generator = MazeGenerator()
    path_finder = PathFinder()

    # Generate maze and collect all coordinates
    result = generator.generate('dfs', 10)
    maze_matrix = result['maze_matrix']

    coordinates_to_check = []

    # Collect start point and end point coordinates
    start_point = result['start_point']
    end_point = result['end_point']

    coordinates_to_check.append(('start_point', start_point))
    coordinates_to_check.append(('end_point', end_point))

    # Collect path coordinates
    path_result = path_finder.solve('bfs', maze_matrix, start_point, end_point)
    if path_result['found_solution']:
        path = path_result['path']
        for i, coord in enumerate(path[:5]):  # Check the first 5 path points
            coordinates_to_check.append((f'path_point_{i}', coord))

    # Verify all coordinate format consistency
    format_issues = []

    for coord_name, coord in coordinates_to_check:
        # Check data type (allow tuple, list or ndarray)
        if not isinstance(coord, (list, np.ndarray, tuple)):
            format_issues.append(f"{coord_name}: Type is not list, ndarray or tuple, but {type(coord)}")
            continue

        # Check length
        if len(coord) != 2:
            format_issues.append(f"{coord_name}: Length is not 2, but {len(coord)}")
            continue

        # Check value type
        try:
            row, col = int(coord[0]), int(coord[1])
            # Check coordinate range validity
            if row < 0 or col < 0:
                format_issues.append(f"{coord_name}: Contains negative coordinate [{row}, {col}]")
            if row >= maze_matrix.shape[0] or col >= maze_matrix.shape[1]:
                format_issues.append(f"{coord_name}: Coordinate [{row}, {col}] exceeds maze boundary")
        except (ValueError, TypeError):
            format_issues.append(f"{coord_name}: Coordinate value cannot be converted to integer: {coord}")

    # Assert no format issues
    assert len(format_issues) == 0, f"Coordinate format issues: {format_issues}"


def test_maze_matrix_format():
    """Test maze matrix format consistency"""
    generator = MazeGenerator()

    # Test maze format generated by different algorithms
    algorithms = ['dfs', 'prim']
    format_issues = []

    for algo in algorithms:
        try:
            result = generator.generate(algo, 10)
            maze_matrix = result['maze_matrix']

            # Check data type
            if not isinstance(maze_matrix, np.ndarray):
                format_issues.append(f"{algo}: Maze matrix is not a numpy array, but {type(maze_matrix)}")
                continue

            # Check data type
            if maze_matrix.dtype != np.uint8:
                format_issues.append(f"{algo}: Data type is not uint8, but {maze_matrix.dtype}")

            # Check dimensions
            if len(maze_matrix.shape) != 2:
                format_issues.append(f"{algo}: Not a 2D array, but {len(maze_matrix.shape)}D")

            # Check value range
            unique_values = np.unique(maze_matrix)
            invalid_values = [v for v in unique_values if v not in [0, 1]]
            if invalid_values:
                format_issues.append(f"{algo}: Contains invalid values {invalid_values}, should only have 0 and 1")

        except Exception as e:
            format_issues.append(f"{algo}: Error occurred while generating maze: {e}")

    assert len(format_issues) == 0, f"Maze matrix format issues: {format_issues}"


def test_path_data_format():
    """Test path data format consistency"""
    generator = MazeGenerator()
    path_finder = PathFinder()

    # Generate test maze
    result = generator.generate('dfs', 12)
    maze_matrix = result['maze_matrix']
    start_point = result['start_point']
    end_point = result['end_point']

    # Test path data format for different algorithms
    algorithms = ['dfs', 'bfs', 'astar']
    format_issues = []

    for algo in algorithms:
        try:
            path_result = path_finder.solve(algo, maze_matrix, start_point, end_point)

            if not path_result['found_solution']:
                continue  # Skip cases where no solution is found

            path = path_result['path']

            # Check path is a list
            if not isinstance(path, list):
                format_issues.append(f"{algo}: Path is not a list, but {type(path)}")
                continue

            # Check path point format
            for i, point in enumerate(path[:3]):  # Check the first 3 points
                if not isinstance(point, (list, np.ndarray, tuple)):
                    format_issues.append(f"{algo}: Path point {i} type error: {type(point)}")
                    continue

                if len(point) != 2:
                    format_issues.append(f"{algo}: Path point {i} length is not 2: {len(point)}")

                try:
                    row, col = int(point[0]), int(point[1])
                except (ValueError, TypeError):
                    format_issues.append(f"{algo}: Path point {i} cannot be converted to integer: {point}")

            # Check statistics data type
            if not isinstance(path_result['path_length'], int):
                format_issues.append(f"{algo}: path_length is not an integer: {type(path_result['path_length'])}")

            if not isinstance(path_result['explored_nodes_count'], int):
                format_issues.append(f"{algo}: explored_nodes_count is not an integer: {type(path_result['explored_nodes_count'])}")

            if not isinstance(path_result['search_time'], (int, float)):
                format_issues.append(f"{algo}: search_time is not a numeric value: {type(path_result['search_time'])}")

        except Exception as e:
            format_issues.append(f"{algo}: Error occurred during path search: {e}")

    assert len(format_issues) == 0, f"Path data format issues: {format_issues}"
